def generate_all():
    """汇总所有数据"""
    all_pairs = []

    # 1. 基础对话
    for k, v in BASE.items():
        all_pairs.append((k, v))

    # 2. 模板生成
    all_pairs.extend(generate_from_templates())

    # 3. 多轮对话
    all_pairs.extend(generate_multi_turn())

    # 4. 数据增强
    all_pairs = augment(all_pairs)

    # 去重
    seen = set()
    unique = []
    for inp, out in all_pairs:
        key = (inp.strip(), out.strip())
        if key not in seen and key[0] and key[1]:
            seen.add(key)
            unique.append({"input": key[0], "output": key[1]})

    random.shuffle(unique)
    return unique


