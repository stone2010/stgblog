def generate_from_templates():
    """从模板生成大量数据"""
    pairs = []

    for pattern_in, pattern_out in TEMPLATE_PATTERNS:
        # 检查模板中有哪些占位符
        placeholders = []
        for name, pool in [("topic", TOPICS), ("emotion", EMOTIONS), ("action", ACTIONS),
                           ("time", TIMES), ("place", PLACES), ("person", PERSONS)]:
            if "{" + name + "}" in pattern_in:
                placeholders.append((name, pool))

        if len(placeholders) == 1:
            # 单变量模板
            name, pool = placeholders[0]
            for val in pool:
                inp = pattern_in.replace("{" + name + "}", val)
                out = pattern_out.replace("{" + name + "}", val)
                pairs.append((inp, out))

        elif len(placeholders) == 2:
            # 双变量模板 - 取子集避免组合爆炸
            (_, pool1), (_, pool2) = placeholders
            for v1 in pool1[:10]:  # 限制第一个变量
                for v2 in pool2[:8]:  # 限制第二个变量
                    inp = pattern_in
                    out = pattern_out
                    for (name, _), val in zip(placeholders, [v1, v2]):
                        inp = inp.replace("{" + name + "}", val)
                        out = out.replace("{" + name + "}", val)
                    pairs.append((inp, out))

        elif len(placeholders) == 3:
            # 三变量模板 - 更小的子集
            (_, pool1), (_, pool2), (_, pool3) = placeholders
            for v1 in pool1[:5]:
                for v2 in pool2[:4]:
                    for v3 in pool3[:3]:
                        inp = pattern_in
                        out = pattern_out
                        for (name, _), val in zip(placeholders, [v1, v2, v3]):
                            inp = inp.replace("{" + name + "}", val)
                            out = out.replace("{" + name + "}", val)
                        pairs.append((inp, out))

    return pairs


def augment(pairs):
    """数据增强"""
    prefixes = ["嗯，", "那个，", "对了，", "话说，", "诶，", "啊，", "哦，", ""]
    suffixes = ["", "呢", "呀", "吧", "啊", "哦", "嗯", "哈", "诶"]
    puncts = ["", "。", "！", "？", "~", "～"]

    augmented = []
    for inp, out in pairs:
        augmented.append((inp, out))

        # 前缀变体
        for p in prefixes:
            if p and random.random() < 0.25:
                augmented.append((p + inp, out))

        # 后缀变体
        for s in suffixes:
            if s and random.random() < 0.25:
                augmented.append((inp + s, out))

        # 标点变体
        for pt in puncts:
            if pt and random.random() < 0.15:
                augmented.append((inp + pt, out))

        # 大小写/繁简变体 (模拟)
        if random.random() < 0.1:
            augmented.append((inp.replace("我", "偶"), out))
        if random.random() < 0.1:
            augmented.append((inp.replace("你", "妳"), out))

    return augmented


