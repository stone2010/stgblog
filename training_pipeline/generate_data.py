def build_vocab(qa_pairs):
    char_counts = {}
    for user, assistant in qa_pairs:
        for char in user + assistant:
            char_counts[char] = char_counts.get(char, 0) + 1

    sorted_chars = sorted(char_counts.items(), key=lambda x: -x[1])
    vocab_chars = ['<PAD>', '<START>', '<END>', '<UNK>'] + [char for char, _ in sorted_chars[:VOCAB_SIZE - 4]]
    vocab = {char: idx for idx, char in enumerate(vocab_chars)}

    return vocab, vocab_chars


def generate_qa_pairs(count=100000):
    qa_pairs = []
    qa_pairs.extend(BASE_PAIRS)

    generated_count = len(BASE_PAIRS)

    while generated_count < count:
        pattern = random.choice(ROMANTIC_PATTERNS)
        topic = random.choice(ROMANTIC_TOPICS)

        user = pattern[0].replace('{topic}', topic)
        assistant = pattern[1].replace('{topic}', topic)

        if len(user) <= 30 and len(assistant) <= 50:
            qa_pairs.append((user, assistant))
            generated_count += 1

            if generated_count % 10000 == 0:
                print(f'Generated {generated_count} QA pairs...')

    return qa_pairs


def save_data(qa_pairs, vocab, vocab_chars):
    output_dir = Path(__file__).parent.parent / 'public' / 'ai_data'
    output_dir.mkdir(parents=True, exist_ok=True)

    with open(output_dir / 'romantic_train.jsonl', 'w', encoding='utf-8') as f:
        for user, assistant in qa_pairs:
            f.write(json.dumps({'user': user, 'assistant': assistant}, ensure_ascii=False) + '\n')

    with open(output_dir / 'vocab.json', 'w', encoding='utf-8') as f:
        json.dump(vocab, f, ensure_ascii=False)

    with open(output_dir / 'vocab_chars.txt', 'w', encoding='utf-8') as f:
        for char in vocab_chars:
            f.write(char + '\n')

    print(f'\nSaved {len(qa_pairs)} QA pairs')
    print(f'Vocabulary size: {len(vocab)}')
    print(f'Data saved to {output_dir}')


def main():
    print('Generating romantic companion dataset...')
    print('=' * 60)

    qa_pairs = generate_qa_pairs(100000)
    vocab, vocab_chars = build_vocab(qa_pairs)
    save_data(qa_pairs, vocab, vocab_chars)


if __name__ == '__main__':
    main()