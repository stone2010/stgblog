// 变量替换
function fillTemplate(template, memory = {}) {
  let result = template;
  if (memory.userName) {
    result = result.replace(/{称呼}/g, memory.userName);
    result = result.replace(/{name}/g, memory.userName);
  }
  if (memory.userTitle) {
    result = result.replace(/{title}/g, memory.userTitle);
  }
  // 如果模板里有 {memory} 占位符但没数据，删掉它
  result = result.replace(/\s*{memory}\s*/g, ' ');
  result = result.replace(/{memory}/g, '');
  return result.trim();
}

// 随机选择（避免连续重复）
function randomChoice(arr, lastIndex = -1) {
  if (arr.length === 0) return '';
  if (arr.length === 1) return arr[0];

  let index;
  let attempts = 0;
  do {
    index = Math.floor(Math.random() * arr.length);
    attempts++;
  } while (index === lastIndex && attempts < 5);

  return { text: arr[index], index };
}

class ResponseGenerator {
  constructor() {
    this.templates = TEMPLATES;
    this.emojis = EMOJIS;
    this.lastResponseIndex = {}; // 记录每种情感上次用的模板索引
  }

  // 生成回复
  generate(emotion, intensity = 0.5, memory = {}, options = {}) {
    const templates = this.templates[emotion] || this.templates.calm;
    const lastIndex = this.lastResponseIndex[emotion] ?? -1;

    let { text, index } = randomChoice(templates, lastIndex);
    this.lastResponseIndex[emotion] = index;

    // 填充模板
    text = fillTemplate(text, memory);

    // 根据强度决定是否加 emoji
    const shouldAddEmoji = Math.random() < (0.3 + intensity * 0.4);
    if (shouldAddEmoji) {
      const emojiArr = this.emojis[emotion] || this.emojis.calm;
      const emoji = emojiArr[Math.floor(Math.random() * emojiArr.length)];
      // 50% 概率 emoji 在前，50% 在后
      text = Math.random() < 0.5 ? `${emoji} ${text}` : `${text} ${emoji}`;
    }

    // 高强度情感增加安慰力度
    if (intensity > 0.7 && ['sad', 'angry', 'anxious', 'lonely'].includes(emotion)) {
      const extra = ['抱抱', '我懂', '我在', '别怕'];
      if (Math.random() < 0.4 && !text.includes(extra[0])) {
        text = `${text}，${extra[Math.floor(Math.random() * extra.length)]}`;
      }
    }

    return {
      text,
      emotion,
      intensity,
    };
  }

  // 主动关心
  getProactive() {
    return PROACTIVE_CARE[Math.floor(Math.random() * PROACTIVE_CARE.length)];
  }

  // 短回复引导
  getShortResponse() {
    return SHORT_RESPONSES[Math.floor(Math.random() * SHORT_RESPONSES.length)];
  }

  // 问候回复
  getGreeting(memory = {}) {
    const greetings = [
      `在呢，{name}，想聊点什么？`,
      `我在，{name}，今天怎么样？`,
      `嗨，{name}，我一直都在`,
      `来啦，{name}，最近好吗？`,
      `你好呀，{name}，有什么想说的吗？`,
    ];
    let g = greetings[Math.floor(Math.random() * greetings.length)];
    g = fillTemplate(g, memory);
    if (!memory.userName) {
      g = g.replace(/,\s*,/g, ',').replace(/^,\s*/, '').replace(/\s*,\s*$/g, '');
    }
    return g;
  }
}

